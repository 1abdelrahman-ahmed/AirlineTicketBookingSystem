    private boolean checkEmail() {
        if (email.isEmpty() || email.equals("Email")) {
            message_email.setText("*Enter email");
            message_email.setVisible(true);
            return false;
        }
        if (!email.contains("@gmail.com")) {
            message_email.setText("*Enter valid email");
            message_email.setVisible(true);
            return false;
        }
        if (!userDAO.checkEmailFound(email)) {
            message_email.setText("*Email not found!");
            message_email.setVisible(true);
            return false;
        }
        return true;
    }
    
    private boolean checkPassword() {
        if (password.isEmpty() || password.equals("Password")) {
            message_pass.setText("*Enter Password");
            message_pass.setVisible(true);
            return false;
        }
        return true;
    }
    
