    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:\
        email = txt_email.getText();
        password = txt_password.getText();
        boolean isvalid = true;
        if (!checkEmail()) isvalid = false;
        if (!checkPassword()) isvalid = false;
        if (!isvalid) return;
        if (!userDAO.checkUserLogin(email, password)) {
            message_wrong.setVisible(true);
            return;
        }
        User user = userDAO.getUserByEmail(email);
        AppSession.setCurrentUser(user);
        this.dispose();
        if (user.getRole().equals("user")) new UserHomePage().setVisible(true);
        else new AdminHomePage().setVisible(true);
    }//GEN-LAST:event_btn_loginActionPerformed

