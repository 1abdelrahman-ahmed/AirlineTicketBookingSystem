    private boolean chcckn(String s) {
        if (s.isEmpty()) {
            message_airportname.setText("*Enter airport name");
            message_airportname.setVisible(true);
            return false;
        }
        return true;
    }
    
    private boolean chcckp(String s) {
        if (s.isEmpty()) {
            message_price.setText("*Enter price");
            message_price.setVisible(true);
            return false;
        }
        try {
            Integer.parseInt(s);
            return true;
        } catch (NumberFormatException e) {
            message_price.setText("*Enter valid price");
            message_price.setVisible(true);
            return false;
        }
    }
    
