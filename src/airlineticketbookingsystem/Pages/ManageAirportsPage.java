    private void fillflight_tbl() {
        airports = airportDAO.getAllAirports();
        dtm.setRowCount(0);
        for (Airport airport : airports) {
            int id = airport.getId();
            String airportname = airport.getAirportName();
            int price = airport.getPrice();
            dtm.addRow(new Object[]{id, airportname, price});
        }
        flight_tbl.setModel(dtm);
    }
    
